                    <h3>👥 Scrum Roles & Responsibilities</h3>
                    <ul>
                        <li><strong>Product Owner:</strong> Defined user requirements and acceptance criteria</li>
                        <li><strong>Scrum Master:</strong> Facilitated daily standups and removed blockers</li>
                        <li><strong>Development Team:</strong> Cross-functional team handling all development aspects</li>
                    </ul>
                    
                    <h3>🔄 Scrum Events</h3>
                    <ul>
                        <li><strong>Daily Standups:</strong> 15-minute sync meetings to discuss progress and blockers</li>
                        <li><strong>Sprint Planning:</strong> 2-hour meetings to plan upcoming sprint work</li>
                        <li><strong>Sprint Review:</strong> Demo completed features to stakeholders</li>
                        <li><strong>Sprint Retrospective:</strong> Reflect on process improvements</li>
                    </ul>
                    
                    <h3>✅ Definition of Done</h3>
